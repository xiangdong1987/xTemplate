namespace Component;

use Interfaces\Dom;

class XForm extends XTemplate
{
    public $is_dynamic = false;

    public $content;

    public $contentS;
    public $contentE;
    public $params;
    /**
     * @var Dom
     */
    public $decorate;

    function __construct($title, $form_name, $method, $url)
    {
        parent::__construct();
        $this->params = [
            'title' => $title,
            'form_name' => $form_name,
            'method' => $method,
            'url' => $url
        ];
    }

    function contentS()
    {
        parent::contentS(); // TODO: Change the autogenerated stub
        if ($this->isElement) {
            $this->contentS = $this->fetch('element/form_start.php', [
                'method' => $this->params['method'],
                'action' => $this->params['url'],
                'title' => $this->params['title'],
                'form_name' => $this->params['form_name'],
            ]);
        } else {
            $this->contentS = $this->fetch('include/form_start.php', [
                'method' => $this->params['method'],
                'action' => $this->params['url'],
                'title' => $this->params['title'],
                'form_name' => $this->params['form_name'],
            ]);
        }
        echo $this->contentS;
    }

    function contentE()
    {
        parent::contentE(); // TODO: Change the autogenerated stub
        if ($this->isElement) {
            $this->contentE = $this->fetch('element/form_end.php', []);
        } else {
            $this->contentE = $this->fetch('include/form_end.php', []);
        }
        echo $this->contentE;
    }

    function decorate(Dom $dom)
    {
        $this->decorate = $dom;
        return $dom;
    }

    function show(Dom $root)
    {
        parent::show($root);
    }
}